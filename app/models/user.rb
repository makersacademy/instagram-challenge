  has_many :posts, dependent: :destroy
  has_many :comments, dependent: :destroy
  has_many :likes, dependent: :destroy
  has_many :liked_photos, through: :likes, source: :post, dependent: :destroy

  def liked?(post)
    liked_photos.include?(post)
  end
end
